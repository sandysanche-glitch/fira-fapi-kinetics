def first_time_at_fraction(nt: pd.DataFrame, frac: float) -> float:
    if nt.empty:
        return np.nan
    n_final = int(nt["N"].iloc[-1])
    target = math.ceil(frac * n_final)
    rows = nt.loc[nt["N"] >= target]
    return float(rows["t_s"].iloc[0]) if not rows.empty else np.nan


def summarize_dataset(nt_df: pd.DataFrame, rate_df: pd.DataFrame, events_df: pd.DataFrame,
                      growth_df: Optional[pd.DataFrame]) -> Dict[str, float]:
    out = {}

    if not nt_df.empty:
        out["N_final"] = int(nt_df["N"].iloc[-1])
        out["onset_s"] = float(nt_df["t_s"].iloc[0])
        out["t_last_s"] = float(nt_df["t_s"].iloc[-1])
        out["t10_s"] = first_time_at_fraction(nt_df, 0.10)
        out["t50_s"] = first_time_at_fraction(nt_df, 0.50)
        out["t90_s"] = first_time_at_fraction(nt_df, 0.90)
    else:
        out.update({k: np.nan for k in ["N_final", "onset_s", "t_last_s", "t10_s", "t50_s", "t90_s"]})

    if not rate_df.empty:
        idx = rate_df["dNdt_per_s"].idxmax()
        out["peak_dNdt_per_s"] = float(rate_df.loc[idx, "dNdt_per_s"])
        out["t_peak_dNdt_s"] = float(rate_df.loc[idx, "t_center_s"])
    else:
        out["peak_dNdt_per_s"] = np.nan
        out["t_peak_dNdt_s"] = np.nan

    out["n_events_rows"] = int(len(events_df))

    if growth_df is not None and not growth_df.empty:
        idx2 = growth_df["dRdt_px_per_s"].abs().idxmax()
        out["R_eq_start_px"] = float(growth_df["R_px"].iloc[0])
        out["R_eq_end_px"] = float(growth_df["R_px"].iloc[-1])
        out["peak_abs_dReqdt_px_per_s"] = float(abs(growth_df.loc[idx2, "dRdt_px_per_s"]))
        out["t_peak_abs_dReqdt_s"] = float(growth_df.loc[idx2, "t_s"])
        if "R_um" in growth_df.columns:
            out["R_eq_start_um"] = float(growth_df["R_um"].iloc[0])
            out["R_eq_end_um"] = float(growth_df["R_um"].iloc[-1])
            out["peak_abs_dReqdt_um_per_s"] = float(abs(growth_df.loc[idx2, "dRdt_um_per_s"]))
    else:
        out["R_eq_start_px"] = np.nan
        out["R_eq_end_px"] = np.nan
        out["peak_abs_dReqdt_px_per_s"] = np.nan
        out["t_peak_abs_dReqdt_s"] = np.nan
        out["R_eq_start_um"] = np.nan
        out["R_eq_end_um"] = np.nan
        out["peak_abs_dReqdt_um_per_s"] = np.nan

    return out


