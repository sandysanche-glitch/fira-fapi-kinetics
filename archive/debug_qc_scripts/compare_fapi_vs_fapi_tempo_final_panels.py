_FRAME_TIME_RE = re.compile(r"_t([0-9]+(?:\.[0-9]+)?)ms", re.IGNORECASE)


def _extract_time_ms_from_frame_id(frame_id_val) -> float:
    if pd.isna(frame_id_val):
        return np.nan
    m = _FRAME_TIME_RE.search(str(frame_id_val))
    return float(m.group(1)) if m else np.nan


def load_global_frame_growth(frame_kin_csv: Path, dataset_name: str) -> Optional[pd.DataFrame]:
    """
    Builds a global growth proxy from frame_kinetics.csv:
      t_s, t_ms, R_px, dRdt_px_per_s (+ optional um columns)
    Uses largest object per frame (by area or R_px) as "global transformed region".
    """
    if not frame_kin_csv.exists():
        print(f"[WARN] Missing frame_kinetics for {dataset_name}: {frame_kin_csv}")
        return None

    df = pd.read_csv(frame_kin_csv)
    if df.empty:
        print(f"[WARN] Empty frame_kinetics for {dataset_name}: {frame_kin_csv}")
        return None

    # Time handling:
    # In your files, time_ms is often constant 0.0, so parse from frame_id.
    use_time_ms = None
    if "time_ms" in df.columns:
        tms = safe_numeric(df["time_ms"])
        if tms.notna().sum() > 0 and (tms.max() - tms.min()) > 1e-12:
            use_time_ms = tms

    if use_time_ms is None:
        if "frame_id" not in df.columns:
            print(f"[WARN] No usable time column in {frame_kin_csv}")
            return None
        use_time_ms = df["frame_id"].map(_extract_time_ms_from_frame_id)

    df["t_ms_use"] = safe_numeric(use_time_ms)
    df["t_s"] = df["t_ms_use"] / 1000.0

    # Radius handling
    if "R_px" in df.columns:
        df["R_px_use"] = safe_numeric(df["R_px"])
    elif "area_px" in df.columns:
        area = safe_numeric(df["area_px"])
        df["R_px_use"] = np.sqrt(area / np.pi)
    else:
        print(f"[WARN] No R_px/area_px in {frame_kin_csv}")
        return None

    # Largest object per frame as global proxy
    if "area_px" in df.columns:
        df["area_px_num"] = safe_numeric(df["area_px"])
        df = df.sort_values(["t_s", "area_px_num"], ascending=[True, False])
    else:
        df = df.sort_values(["t_s", "R_px_use"], ascending=[True, False])

    g = (
        df.dropna(subset=["t_s", "R_px_use"])
          .groupby("t_s", as_index=False)
          .first()
          .copy()
    )

    if g.empty:
        print(f"[WARN] No valid rows after grouping for {dataset_name}")
        return None

    # Keep clean columns
    out = pd.DataFrame({
        "t_s": safe_numeric(g["t_s"]),
        "t_ms": safe_numeric(g["t_s"]) * 1000.0,
        "R_px": safe_numeric(g["R_px_use"]),
    }).dropna().sort_values("t_s").reset_index(drop=True)

    if len(out) < 3:
        print(f"[WARN] Too few points for growth derivative ({dataset_name})")
        return None

    # Numerical derivative dR/dt [px/s]
    t = out["t_s"].to_numpy(dtype=float)
    r = out["R_px"].to_numpy(dtype=float)
    # gradient handles nonuniform spacing
    dRdt = np.gradient(r, t)
    out["dRdt_px_per_s"] = dRdt
    out["dataset"] = dataset_name
    out["growth_source"] = str(frame_kin_csv)

    # Optional physical units
    if PX_TO_UM is not None:
        out["R_um"] = out["R_px"] * PX_TO_UM
        out["dRdt_um_per_s"] = out["dRdt_px_per_s"] * PX_TO_UM

    return out


