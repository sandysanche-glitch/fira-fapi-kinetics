def _make_event_hist(events_ms: np.ndarray, bin_ms: float) -> Tuple[np.ndarray, np.ndarray]:
    if len(events_ms) == 0:
        return np.array([]), np.array([])
    tmin = 0.0
    tmax = float(np.nanmax(events_ms))
    edges = np.arange(tmin, tmax + bin_ms + 1e-9, bin_ms)
    if len(edges) < 2:
        edges = np.array([0.0, bin_ms])
    counts, edges = np.histogram(events_ms, bins=edges)
    centers = 0.5 * (edges[:-1] + edges[1:])
    return centers, counts


def plot_event_hist(events_fapi: pd.DataFrame, events_tempo: pd.DataFrame, out_dir: Path, bin_ms: float) -> None:
    c1, n1 = _make_event_hist(events_fapi["t_ms"].to_numpy(dtype=float), bin_ms)
    c2, n2 = _make_event_hist(events_tempo["t_ms"].to_numpy(dtype=float), bin_ms)

    plt.figure(figsize=(10, 6))
    if len(c1):
        plt.plot(c1, n1, marker="o", label=f"FAPI (n={len(events_fapi)})")
    if len(c2):
        plt.plot(c2, n2, marker="o", label=f"FAPI-TEMPO (n={len(events_tempo)})")
    plt.xlabel("time (ms)")
    plt.ylabel(f"events / {int(bin_ms)} ms bin")
    plt.title(f"Nucleation event histogram (bin = {int(bin_ms)} ms)")
    plt.legend()
    savefig(out_dir / "compare_event_hist_FAPI_vs_FAPI-TEMPO.png")


def plot_event_hist_rate(events_fapi: pd.DataFrame, events_tempo: pd.DataFrame, out_dir: Path, bin_ms: float) -> None:
    c1, n1 = _make_event_hist(events_fapi["t_ms"].to_numpy(dtype=float), bin_ms)
    c2, n2 = _make_event_hist(events_tempo["t_ms"].to_numpy(dtype=float), bin_ms)

    scale = 1000.0 / bin_ms  # convert counts/bin to counts/s
    plt.figure(figsize=(10, 6))
    if len(c1):
        plt.plot(c1, n1 * scale, marker="o", label=f"FAPI (n={len(events_fapi)})")
    if len(c2):
        plt.plot(c2, n2 * scale, marker="o", label=f"FAPI-TEMPO (n={len(events_tempo)})")
    plt.xlabel("time (ms)")
    plt.ylabel("event histogram rate (1/s)")
    plt.title(f"Nucleation event histogram rate (bin = {int(bin_ms)} ms)")
    plt.legend()
    savefig(out_dir / "compare_event_hist_rate_FAPI_vs_FAPI-TEMPO.png")


def plot_tau_robustness_panel(compare_dir: Path, out_dir: Path) -> None:
    """
    Best-effort tau robustness panel from kinetics_tau{tag}_Nt.csv in FAPI-TEMPO stable folder.
    FAPI is fixed (tau0p3 matched reconstruction).
    """
    # FAPI fixed
    fapi_nt_path = FAPI_MATCHED_DIR / "kinetics_tau0p3_Nt.csv"
    if not fapi_nt_path.exists():
        print("[WARN] FAPI tau0p3 Nt not found; skipping tau robustness panel.")
        return
    fapi_nt = load_Nt_curve(fapi_nt_path, "FAPI")

    curves = []
    for tag in TAU_SWEEP_TAGS:
        p = TEMPO_STABLE_DIR / f"kinetics_tau{tag}_Nt.csv"
        if p.exists():
            curves.append((tag, load_Nt_curve(p, f"FAPI-TEMPO tau={tag.replace('p','.')}")))

    if not curves:
        print("[WARN] No FAPI-TEMPO tau sweep Nt files found; skipping tau robustness panel.")
        return

    plt.figure(figsize=(10, 6))
    plt.step(fapi_nt["t_s"] * 1000.0, fapi_nt["N"], where="post", linewidth=2.5, label=f"FAPI fixed (tau=0.3, n={int(fapi_nt['N'].iloc[-1])})")
    for tag, nt in curves:
        plt.step(nt["t_s"] * 1000.0, nt["N"], where="post", label=f"FAPI-TEMPO tau={tag.replace('p','.')} (n={int(nt['N'].iloc[-1])})")
    plt.xlabel("time (ms)")
    plt.ylabel("N(t)")
    plt.title("Tau robustness panel (FAPI fixed vs FAPI-TEMPO tau sweep)")
    plt.legend()
    savefig(out_dir / "tau_robustness_panel_FAPI_fixed_vs_TEMPO_tau_sweep.png")


