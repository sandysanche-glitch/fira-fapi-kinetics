def load_rate_curve(path: Path, dataset_name: str) -> pd.DataFrame:
    df = read_csv(path, f"rate curve ({dataset_name})").copy()

    # Flexible column handling
    if "t_center_s" not in df.columns:
        if "t_s" in df.columns:
            df["t_center_s"] = safe_numeric(df["t_s"])
        elif "t_center_ms" in df.columns:
            df["t_center_s"] = safe_numeric(df["t_center_ms"]) / 1000.0

    if "dNdt_per_s" not in df.columns:
        if "rate" in df.columns:
            df["dNdt_per_s"] = safe_numeric(df["rate"])

    req = ["t_center_s", "dNdt_per_s"]
    missing = [c for c in req if c not in df.columns]
    if missing:
        raise ValueError(f"Rate curve missing columns {missing} in {path}")

    df["t_center_s"] = safe_numeric(df["t_center_s"])
    df["dNdt_per_s"] = safe_numeric(df["dNdt_per_s"])
    df = df.dropna(subset=["t_center_s", "dNdt_per_s"]).sort_values("t_center_s").reset_index(drop=True)
    df["dataset"] = dataset_name
    return df


def load_Nt_curve(path: Path, dataset_name: str) -> pd.DataFrame:
    df = read_csv(path, f"Nt curve ({dataset_name})").copy()

    if "t_s" not in df.columns:
        if "t_ms" in df.columns:
            df["t_s"] = safe_numeric(df["t_ms"]) / 1000.0

    if "N" not in df.columns:
        raise ValueError(f"Nt curve missing 'N' column in {path}")

    df["t_s"] = safe_numeric(df["t_s"])
    df["N"] = safe_numeric(df["N"])
    df = df.dropna(subset=["t_s", "N"]).sort_values("t_s").reset_index(drop=True)
    df["dataset"] = dataset_name
    return df


