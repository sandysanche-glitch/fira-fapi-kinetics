def load_events_csv(path: Path, dataset_name: str) -> pd.DataFrame:
    df = read_csv(path, f"events ({dataset_name})").copy()

    # Support t_s or t_ms
    if "t_s" not in df.columns:
        if "t_ms" in df.columns:
            df["t_s"] = safe_numeric(df["t_ms"]) / 1000.0

    if "t_ms" not in df.columns:
        if "t_s" in df.columns:
            df["t_ms"] = safe_numeric(df["t_s"]) * 1000.0

    req = ["t_s", "t_ms"]
    missing = [c for c in req if c not in df.columns]
    if missing:
        raise ValueError(f"Events file missing columns {missing} in {path}")

    df["t_s"] = safe_numeric(df["t_s"])
    df["t_ms"] = safe_numeric(df["t_ms"])
    df = df.dropna(subset=["t_s", "t_ms"]).sort_values("t_s").reset_index(drop=True)
    df["dataset"] = dataset_name
    return df


